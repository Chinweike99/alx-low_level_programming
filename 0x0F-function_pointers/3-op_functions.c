#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - Function to perform addition
 * @a: An integer
 * @b: An integer
 * Return: Rturn Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function to perform Subtraction
 * @a: An integer
 * @b: An integer
 * Return: Rturn sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function to perform Multiplication
 * @a: An integer
 * @b: An integer
 * Return: Rturn Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function to perform Division
 * @a: An integer
 * @b: An integer
 * Return: Rturn Div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function to perform modulus Operation
 * @a: An integer
 * @b: An integer
 * Return: Rturn Modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
